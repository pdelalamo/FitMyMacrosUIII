class Meal {

    id: any;
    name: any;
    calories: any;
    protein: any;
    carbs: any;
    fat: any;

    constructor(id: any, name: any, calories: any, protein: any, carbs: any, fat: any) {
        this.id = id;
        this.name = name;
        this.calories = calories;
        this.protein = protein;
        this.carbs = carbs;
        this.fat = fat;
    }
}

export default Meal;
